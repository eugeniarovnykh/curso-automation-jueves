package com.icaro.clase2;

public class P2ImprimirDatos {
    public static void main(String[] args) {
        System.out.println(10); // Se imprime un entero
        System.out.println(112312541234545L); // Se imprime un long
        System.out.println('c'); // Se imprime un char
        System.out.println(12.342343); // Se imprime un double
        System.out.println(true); // Se imprime un booleano
        System.out.println(false); // Se imprime un booleano
    }
}